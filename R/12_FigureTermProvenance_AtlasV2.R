if (!exists("write_csv2")) write_csv2 <- function(x, path) readr::write_csv(x, path)

safe_chr <- function(x) {
  x <- as.character(x)
  x[is.na(x)] <- ""
  x
}

safe_num <- function(x, default = NA_real_) {
  y <- suppressWarnings(as.numeric(x))
  ifelse(is.na(y), default, y)
}

safe_int <- function(x, default = 0L) {
  y <- suppressWarnings(as.integer(x))
  ifelse(is.na(y), default, y)
}

norm_term <- function(x) {
  x <- safe_chr(x)
  x <- stringr::str_trim(x)
  x <- gsub("^hit__", "", x)
  x <- gsub("^node__", "", x)
  x
}

file_base <- function(x) basename(as.character(x))

read_optional_table <- function(prefix) {
  exact <- file.path(DIR_TABLE, sprintf("%s_%s__%s.csv", prefix, CORPUS_TAG, DIC_TAG))
  if (file.exists(exact)) {
    log_msg("Loaded table:", exact)
    return(readr::read_csv(exact, show_col_types = FALSE))
  }
  regex <- sprintf("^%s_.*__%s\\.csv$", prefix, DIC_TAG)
  alt <- find_latest_file(DIR_TABLE, regex)
  if (!is.na(alt) && file.exists(alt)) {
    log_msg("Loaded latest matching table:", alt)
    return(readr::read_csv(alt, show_col_types = FALSE))
  }
  log_msg("Optional table not found; using empty table for prefix:", prefix)
  tibble::tibble()
}

read_optional_file <- function(filename_regex) {
  f <- find_latest_file(DIR_TABLE, filename_regex)
  if (!is.na(f) && file.exists(f)) {
    log_msg("Loaded latest matching file:", f)
    return(readr::read_csv(f, show_col_types = FALSE))
  }
  log_msg("Optional file not found; using empty table for regex:", filename_regex)
  tibble::tibble()
}

hit_col <- function(term) paste0("hit__", term)

get_vec <- function(df, term) {
  nm <- hit_col(term)
  if (!nm %in% names(df)) return(integer(nrow(df)))
  as.integer(ifelse(is.na(df[[nm]]), 0L, df[[nm]] > 0L))
}

cohit_count <- function(df, term1, term2) {
  x <- get_vec(df, term1)
  y <- get_vec(df, term2)
  as.integer(sum(x == 1L & y == 1L, na.rm = TRUE))
}

first_present_col <- function(df, candidates) {
  hit <- intersect(candidates, names(df))
  if (length(hit)) hit[1] else NA_character_
}

col_chr <- function(df, col, default = "") {
  if (!is.na(col) && col %in% names(df)) return(safe_chr(df[[col]]))
  rep(default, nrow(df))
}

col_num <- function(df, col, default = NA_real_) {
  if (!is.na(col) && col %in% names(df)) return(safe_num(df[[col]], default = default))
  rep(default, nrow(df))
}

col_int <- function(df, col, default = NA_integer_) {
  if (!is.na(col) && col %in% names(df)) return(safe_int(df[[col]], default = default))
  rep(default, nrow(df))
}

col_first_chr <- function(df, candidates, default = "") {
  col <- first_present_col(df, candidates)
  col_chr(df, col, default = default)
}

col_first_num <- function(df, candidates, default = NA_real_) {
  col <- first_present_col(df, candidates)
  col_num(df, col, default = default)
}

paste_nonempty <- function(x, sep = ";") {
  x <- unique(safe_chr(x))
  x <- x[nzchar(x)]
  if (!length(x)) "" else paste(x, collapse = sep)
}

collapse_max <- function(x) {
  x <- safe_num(x)
  if (all(is.na(x))) NA_real_ else max(x, na.rm = TRUE)
}

# Create a metric summary string from whatever common metric columns a source table has.
metric_blob <- function(df) {
  metric_cols <- intersect(
    c("n11", "cohit_n", "co_mentioned_articles", "unique_term_outcome_articles",
      "unique_domain_outcome_articles", "summed_term_outcome_cohits", "figure_size_metric",
      "articles", "signed_articles", "direction_articles", "total_articles", "total_support",
      "score_q", "support_hmean", "AB_n11", "BC_n11", "AC_n11", "npmi", "lift", "q",
      "balance", "direction_balance", "signed_balance", "weighted_direction_balance"),
    names(df)
  )
  if (!length(metric_cols)) return(rep("", nrow(df)))
  purrr::map_chr(seq_len(nrow(df)), function(i) {
    vals <- purrr::map_chr(metric_cols, function(m) {
      val <- df[[m]][i]
      if (length(val) == 0 || is.na(val)) return("")
      paste0(m, "=", as.character(signif(safe_num(val, default = val), 4)))
    })
    paste(vals[nzchar(vals)], collapse = "; ")
  })
}

