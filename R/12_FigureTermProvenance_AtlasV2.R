make_event_rows <- function(df, source_file, figure, panel, figure_role, term_cols,
                            outcome_cols = c("outcome", "C", "clinical_outcome"),
                            selection_rule = "", supplementary = FALSE) {
  if (!nrow(df)) return(tibble::tibble())
  tcols <- intersect(term_cols, names(df))
  if (!length(tcols)) return(tibble::tibble())
  ocol <- first_present_col(df, outcome_cols)
  metric <- metric_blob(df)
  purrr::map_dfr(tcols, function(tc) {
    tibble::tibble(
      figure = figure,
      panel = panel,
      supplementary = supplementary,
      figure_role = figure_role,
      term = norm_term(df[[tc]]),
      source_column = tc,
      source_file = file_base(source_file),
      source_row = seq_len(nrow(df)),
      outcome = if (!is.na(ocol)) norm_term(df[[ocol]]) else "",
      selection_rule = selection_rule,
      source_metric_blob = metric
    )
  }) |>
    dplyr::filter(term != "", !is.na(term)) |>
    dplyr::distinct()
}

read_source <- function(prefix, regex = NULL) {
